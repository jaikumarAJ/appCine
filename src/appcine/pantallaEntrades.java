    /**
     * Comprova que un jcombobox ja contengui un valor
     *
     * @param valor
     * @param combo
     * @return
     */
    private boolean teValor(String valor, javax.swing.JComboBox combo) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).equals(valor)) {
                return true;
            }
        }
        return false;
    }

